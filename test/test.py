 # 使用正则表达式一次性匹配完整的句子（包括标点符号）
# 匹配非结束符字符+结束符+可选空白字符
import re

text="""主要改进：
​​更优雅的句子分割​​：使用正则表达式分割句子时保留分隔符，确保句子完整性
​​智能分割点查找​​：使用正则表达式查找所有可能的分割点（标点和空格），然后选择最接近末尾的分割点
​​避免重复逻辑​​：不再重复使用正则表达式查找分割点，而是使用更系统的方法
​​保持语义完整性​​：优先在标点处分割，其次在空格处分割，最后才强制分割
这个实现更加优雅和系统化，通过使用正则表达式查找所有可能的分割点，然后选择最合适的一个，避免了冗长的条件判断链。    
"""
sentence_pattern = r'[^。！？\n；，.!?;]+[。！？\n；，.!?;]\s*'
sentences = re.findall(sentence_pattern, text)

print(sentences)        